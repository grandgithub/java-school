package com.endava.timbuchalka.s3;

public class S3_30_PoundsToKilograms {

    public static void main(String[] args) {

        int pounds = 2;
        float measure = 0.4535927f;
        float kilograms = pounds * measure;
        System.out.println(pounds + " pounds are equal to " + kilograms + " kilograms");
    }
}
